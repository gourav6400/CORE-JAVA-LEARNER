package decisionControlStatements;

public class Loops {

	public static void main(String[] args) {
		for(int i=2;i<1;i++){
			System.out.println(i);
		}
		
		int j=5;
		while(j<2){
			System.out.println(j);
		}
		
		int k=11;
		do{
			System.out.println("Hello Java");
		}while(++k<=3);
		}

	}
